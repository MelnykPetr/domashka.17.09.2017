
<?php
//33.Операторы инкремента и декремента
$a = 1;
$a++; // 2
echo $a . '<br>';
$a--; // 1

echo $a++; // first output, then increase
echo '<br>';

echo $a . '<br>';

echo ++$a; // first increase, then output


$a = 10;
$a += 21; // same as $a = $a + 21
$a -= 15; // same as $a = $a - 15;

$a = 5;
$a *= 2; // same as $a = $a * 2
$a /= 2; // same as $a = $a / 2;